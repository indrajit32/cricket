    public function leaderboard()
    {
        $score_list = DB::select("SELECT t.name,t.logouri,s.total_score,s.t_id from teams as t INNER JOIN (SELECT SUM(score) as total_score,team_id as t_id FROM points GROUP BY team_id ) as s ON t.id = s.t_id ORDER BY total_score DESC");
        
